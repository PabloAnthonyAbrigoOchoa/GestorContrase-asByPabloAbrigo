    public GuardarContraseña guardarEditarContraseña() { //Metodo para las Validaciones del Sistema Tanto Guardar como Editar

        if (txtLetraNombreGuardarContra.getText().isEmpty()) { //Comprobamos si esta vacio el campo que estamos enviando
            JOptionPane.showMessageDialog(rootPane, "El campo LetraDeTuNombre no tiene ningún Dato.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtLetraNombreGuardarContra.requestFocus();//Codigo que sirve para dejar el cursor del mouse en el Campo Vacio
            return null;//Por cuestiones de si tengo algun error
        }
        if (!utilidades.validarLetras(txtLetraNombreGuardarContra.getText().trim())) {//trim sirve para validar que no tenga en cuenta espacios en blanco
            JOptionPane.showMessageDialog(rootPane, "El campo LetraDeTuNombre solo debe contener una Letra.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (txtLetraApellidoGuardarContra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo LetraDeTuApellido no tiene ningún Dato.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtLetraApellidoGuardarContra.requestFocus();
            return null;
        }
        if (!utilidades.validarLetras(txtLetraApellidoGuardarContra.getText().trim())) {//trim sirve para validar que no tenga en cuenta espacios en blanco
            JOptionPane.showMessageDialog(rootPane, "El campo LetraDeTuApellido solo debe contener una Letra.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (txtNumeroCedulaGuardarContra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo UnNumeroDeTúCédula no tiene ningún Dato.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtNumeroCedulaGuardarContra.requestFocus();
            return null;
        }
        if (!utilidades.validarNumeros(txtNumeroCedulaGuardarContra.getText().trim())) {//trim sirve para validar que no tenga en cuenta espacios en blanco
            JOptionPane.showMessageDialog(rootPane, "El campo NumeroDeTúCédula solo debe contener Números del 0 al 9.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return null;
//        } else {
//            JOptionPane.showMessageDialog(rootPane, "El campo Un Numero De Tú Cédula es Correcto.");
        }
        if (txtDiaNacidoGuardarContra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo DíaEnQueNaciste no tiene ningún Dato.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtDiaNacidoGuardarContra.requestFocus();
            return null;
        }
        if (!utilidades.validarNumeros(txtDiaNacidoGuardarContra.getText().trim())) {
            JOptionPane.showMessageDialog(rootPane, "El campo DíaEnQueNaciste solo debe contener Números del 0 al 9.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        if (txtMesNacidoGuardarContra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo MesEnQueNaciste no tiene ningún Dato.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtMesNacidoGuardarContra.requestFocus();
            return null;
        }
        if (!utilidades.validarNumeros(txtMesNacidoGuardarContra.getText().trim())) {
            JOptionPane.showMessageDialog(rootPane, "El campo MesEnQueNaciste solo debe contener Números del 0 al 9.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return null;
        }
        GuardarContraseña guardarContraseña = new GuardarContraseña();
        guardarContraseña.setLetraDeTuNombre(txtLetraNombreGuardarContra.getText());
        guardarContraseña.setLetraDeTuApellido(txtLetraApellidoGuardarContra.getText());
        guardarContraseña.setUnNumeroDeTuCedula(txtNumeroCedulaGuardarContra.getText());
        guardarContraseña.setDiaEnQueNaciste(txtDiaNacidoGuardarContra.getText());
        guardarContraseña.setMesEnQueNaciste(txtMesNacidoGuardarContra.getText());
        return guardarContraseña;
    }

