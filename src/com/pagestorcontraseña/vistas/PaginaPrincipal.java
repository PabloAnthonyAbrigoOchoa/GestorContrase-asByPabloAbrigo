    public CrearContraseña guardarEditarContra() {
        //Utilizamos este Metodo tanto para Guardar como para Editar ya que se repiten los mismo datos
        if (txtUsuarioCrearContra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo Usuario no tiene datos.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtUsuarioCrearContra.requestFocus();
            return null;//Me va retornar nulo si se presenta algun error
        }
        if (txtContraseñaCrearContra.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "El campo Contraseña no tiene datos.", "ERROR", JOptionPane.ERROR_MESSAGE);
            txtContraseñaCrearContra.requestFocus();
            return null;
        }
        CrearContraseña crearContra = new CrearContraseña();
        crearContra.setUsuario(txtUsuarioCrearContra.getText());
        crearContra.setContraseña(txtContraseñaCrearContra.getText());
        return crearContra;

    }


    private void btnEditarContraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarContraActionPerformed
        //Comparamos que la contraseña no sea nula
        if (contraEditaEliminada == null) {
            JOptionPane.showMessageDialog(rootPane, "No hay una Contraseña seleccionada para editar.", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }
        //Comprobamos que la contraseña que estoy editando tenga datos
        CrearContraseña contraseñaEditar = guardarEditarContra();
        if (contraseñaEditar != null) {
            contraseñaEditar.setIdCrearContraseña(contraEditaEliminada.getIdCrearContraseña());
            if (controladorCrearContraseña.editarContraseña(contraseñaEditar)) {
                JOptionPane.showMessageDialog(rootPane, "Contraseña editada con éxito del Sistema.");
                limpiarCamposContraseña();
                contraseñaEditar = null;//Reiniciamos la contraseña para poder editarla
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo editar la Contraseña.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEditarContraActionPerformed

